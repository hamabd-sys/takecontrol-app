export function getSurahFromPage(pageNum) {
  const page = parseInt(pageNum);
  if (isNaN(page) || page < 1 || page > 604) return null;

  for (const entry of SURAH_PAGE_MAP) {
    if (page >= entry.start && page <= entry.end) {
      return entry.surah;
    }
  }
  return null;
}

export function getPageFromSurah(surahNum) {
  const surah = parseInt(surahNum);
  if (isNaN(surah) || surah < 1 || surah > 114) return null;

  const entry = SURAH_PAGE_MAP.find(s => s.surah === surah);
  return entry ? entry.start : null;
}
